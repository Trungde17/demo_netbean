    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/plain");

        // Lấy đối tượng PrintWriter để gửi dữ liệu văn bản về trình duyệt
        java.io.PrintWriter out = response.getWriter();

        // Gửi văn bản đến trình duyệt
        out.println(request.getParameter("index"));
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        ArrayList<Student> studentList = (ArrayList<Student>) session.getAttribute("studentList");
        if (studentList != null) {
            String index = request.getParameter("index");
            int ind = Integer.parseInt(index) - 1;
            studentList.get(ind).randomValues();
        }
        RequestDispatcher rd = getServletContext().getRequestDispatcher("/index.jsp");
        rd.forward(request, response);

    }

